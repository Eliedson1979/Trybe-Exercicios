const valor = 80;

if (valor < 0 || valor > 100) {
    console.log("Fora de intervalo");
}else if (valor >= 90) {
    console.log("A");
}else if (valor >= 80) {
    console.log("B");
}else if (valor >= 70) {
    console.log("C");
}else if (valor >= 60) {
    console.log("D");
}else if (valor >= 50) {    
    console.log("E");
}else {
    console.log("F");
}
